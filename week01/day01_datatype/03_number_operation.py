# 사칙 연산 ( +, - , * , / )
num1 = 3
num2 = 2
res = num1 + num2
print(res)

res = res - num2
print(res)

res = res * num2
print(res)

res = res / num2
print(res)

res = 14 / 3
print(res)

# 나눗셈 후 소숫점 이하 버리기 ( 몫 구하기 )
res = 14 // 3
print(res)

# 나눈 나머지 구하기
res = 14 % 3
print(res)

# ** : 승 값을 나타내는 연산자
res = 9 ** 2
print(res)

