# 문자열 대입 : %s
print("저의 이름은 %s 입니다." % "홍길동")
print("저의 이름은 %s 입니다." % '고길동')
print("제 나이는 %s 살 입니다." % 120)
print("제 나이는 %s 살 입니다." % 122)
print('-'*20)

# 자리 지정
print("저의 이름은 %10s 입니다." % "홍길동")   # 오른쪽 정렬됨
print("저의 이름은 %10s 입니다." % '고길동')   # 오른쪽 정렬됨
print("제 나이는 %10s 살 입니다." % 120)
print("제 나이는 %10s 살 입니다." % 122)
print("저의 이름은 %-10s 입니다." % "홍길동")   # 왼쪽 정렬됨
print("저의 이름은 %-10s 입니다." % '고길동')   # 왼쪽 정렬됨
print('-'*20)

